function restoreVehicleFromName(Name)
	for i,v in ipairs(_G["Settings"]) do
		if type(Name) == "string" and v.Modelname == Name:lower() then
			if fileExists( "client/files/models/"..v.Modelname..".dff" ) then
				engineImportTXD( engineLoadTXD("client/files/models/"..v.Modelname..".txd"), v.ID )
			end
			if fileExists( "client/files/models/"..v.Modelname..".dff" ) then
				engineReplaceModel( engineLoadDFF("client/files/models/"..v.Modelname..".dff"), v.ID )
			end
		end
	end
end

